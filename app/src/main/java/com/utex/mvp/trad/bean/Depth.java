package com.utex.mvp.trad.bean;

import java.util.List;

/**
 * Created by Demon on 2018/5/30.
 */
public class Depth {


    /**
     * asks : []
     * bids : [["1","4","4"]]
     * stock_amount : 0.0000
     * money_amount : 4.00000000
     */

    private String stock_amount;
    private String money_amount;
    private List<List<String>> asks;
    private List<List<String>> bids;

    public String getStock_amount() {
        return stock_amount;
    }

    public void setStock_amount(String stock_amount) {
        this.stock_amount = stock_amount;
    }

    public String getMoney_amount() {
        return money_amount;
    }

    public void setMoney_amount(String money_amount) {
        this.money_amount = money_amount;
    }

    public List<List<String>> getAsks() {
        return asks;
    }

    public void setAsks(List<List<String>> asks) {
        this.asks = asks;
    }

    public List<List<String>> getBids() {
        return bids;
    }

    public void setBids(List<List<String>> bids) {
        this.bids = bids;
    }
}

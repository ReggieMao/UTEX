public class VolVo {

    private String amount;

    private String price;

    private String time;

    private String id;

    private String type;

    public VolVo() {
    }

    public VolVo(String amount, String price, String time, String id, String type) {
        this.amount = amount;
        this.price = price;
        this.time = time;
        this.id = id;
        this.type = type;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }


    @Override
    public String toString() {
        return "VolVo{" +
                "amount='" + amount + '\'' +
                ", price='" + price + '\'' +
                ", time='" + time + '\'' +
                ", id='" + id + '\'' +
                ", type='" + type + '\'' +
                '}';
    }
}

public class WithdrawalDTO {

    private String coin_code;

    private double amount;

    private String withdrawal_address;

    private String tag;

    private String email_code;

    private String tel_code;

    private String google_code;

    public WithdrawalDTO() {

    }

    public WithdrawalDTO(String coin_code, double amount, String withdrawal_address, String email_code, String tel_code, String google_code) {
        this.coin_code = coin_code;
        this.amount = amount;
        this.withdrawal_address = withdrawal_address;
        this.email_code = email_code;
        this.tel_code = tel_code;
        this.google_code = google_code;
    }

    public String getTag() {
        return tag;
    }

    public void setTag(String tag) {
        this.tag = tag;
    }

    public String getCoin_code() {
        return coin_code;
    }

    public void setCoin_code(String coin_code) {
        this.coin_code = coin_code;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public String getWithdrawal_address() {
        return withdrawal_address;
    }

    public void setWithdrawal_address(String withdrawal_address) {
        this.withdrawal_address = withdrawal_address;
    }

    public String getEmail_code() {
        return email_code;
    }

    public void setEmail_code(String email_code) {
        this.email_code = email_code;
    }

    public String getTel_code() {
        return tel_code;
    }

    public void setTel_code(String tel_code) {
        this.tel_code = tel_code;
    }

    public String getGoogle_code() {
        return google_code;
    }

    public void setGoogle_code(String google_code) {
        this.google_code = google_code;
    }
}
